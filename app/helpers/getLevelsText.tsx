export const getLevelsText = (level: string) => {
  if (level === 'Новичок') {
    return 'Что бы получить следующий уровень продолжайте тренироваться'
  }
  if (level === 'Любитель') {
    return 'Что бы получить следующий уровень продолжайте тренироваться'
  }
  if (level === 'Профи') {
    return 'Что бы получить следующий уровень продолжайте тренироваться'
  }
  return 'Что бы получить следующий уровень заполните информацию о себе и завершите одну тренировку'
}
